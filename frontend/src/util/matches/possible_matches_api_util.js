import axios from "axios";

export const getPossibleMatches = (dogId) => {
  return axios.get(`/api/possible_matches/${dogId}`);
};

//create possible matches object that has the possible matches array
export const makePossibleMatches = (dogId) => {
  return axios.post(`/api/possible_matches/${dogId}`);
};

//add dog id to possible matches array
export const addPossibleMatches = (dogId, newDogId) => {
  return axios.patch(`/api/possible_matches/${dogId}/add/${newDogId}`);
};

//move dog id from possible matches to rejected 
export const curvePossibleMatches = (dogId, rejectedDogId) => {
  return axios.patch(`/api/possible_matches/${dogId}/move/${rejectedDogId}`);
};




